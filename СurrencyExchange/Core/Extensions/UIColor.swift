import UIKit

extension UIColor {

    public class var customBlue: UIColor {
        return UIColor(red: 0.25, green: 0.66, blue: 0.96, alpha: 1.00)
    }
    
    public class var customBackground: UIColor {
        return UIColor(red: 0.95, green: 0.96, blue: 0.98, alpha: 1.00)
    }
    
    public class var customGray: UIColor {
        return UIColor(red: 0.67, green: 0.68, blue: 0.74, alpha: 1.00)
    }
}
